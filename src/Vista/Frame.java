    private String obtenerHora() {
        Date fechaseleccionadad = jDateChooser1.getDate();
        SimpleDateFormat dateformat = new SimpleDateFormat("yyyy-MM-dd");
        String fechaformateada = dateformat.format(fechaseleccionadad);
        return fechaformateada;
    }

    private void setHora(String hora) {
        SimpleDateFormat dateformat = new SimpleDateFormat("yyyy-MM-dd");
        try {
            Date parsedDate = dateformat.parse(hora);
            jDateChooser1.setDate(parsedDate);
        } catch (ParseException e) {
            e.printStackTrace();
        }
    }

