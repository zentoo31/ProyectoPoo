    private void txtConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtConsultarActionPerformed
        String codigo = txtCodigo.getText(); // Obtener el código de producto desde el campo de texto
        String nombre = txtNombre.getText(); // Obtener el nombre de producto desde el campo de texto
        Producto producto = null;
        // Llamar al método para consultar el producto
        if (codigo.isEmpty()) {
            producto = Producto.consultarProductoPorNombre(nombre);
        } else {
            producto = Producto.consultarProductoPorCodigo(codigo);
        }

        if (producto != null) {
            // Si se encontró el producto, llenar los campos de texto
            txtCodigo.setText(producto.getCodigo());
            txtNombre.setText(producto.getNombre());
            txtCantidad.setText(String.valueOf(producto.getCantidad()));
            txtUnidad.setText(producto.getUnidad());
            setHora(producto.getVencimiento());
            txtCategoria.setText(producto.getCategoria());
            DeshabilitarBoton();
        } else {
            // Si no se encontró el producto, podrías mostrar un mensaje de error o limpiar los campos
            JOptionPane.showMessageDialog(null, "Producto no encontrado.");
            // Puedes limpiar los campos de esta manera:
            txtNombre.setText("");
            txtCantidad.setText("");
            txtUnidad.setText("");
            jDateChooser1.setDate(null);
            txtCategoria.setText("");
        }

    }//GEN-LAST:event_txtConsultarActionPerformed

